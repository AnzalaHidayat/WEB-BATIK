                    <h2 class="section-heading text-uppercase">Sejarah Batik</h2>
                    <h3 class="section-subheading text-muted">Sejarah batik Indonesia terkait erat dengan perkembangan Kerajaan Majapahit dan penyebaran ajaran Islam di Pulau Jawa. Dalam beberapa catatan, pengembangan batik banyak dilakukan pada zaman Kesultanan Mataram, lalu berlanjut pada zaman Kasunanan Surakarta dan Kesultanan Yogyakarta. Kesenian batik di Indonesia telah dikenal sejak zaman Kerajaan Majapahit dan terus berkembang sampai kerajaan berikutnya beserta raja-rajanya. Kesenian batik secara umum meluas di Indonesia dan secara khusus di pulau Jawa setelah akhir abad ke-18 atau awal abad ke-19.Teknik batik sendiri telah diketahui lebih dari 1.000 tahun, kemungkinan berasal dari Mesir kuno atau Sumeria. Teknik batik meluas di beberapa negara di Afrika Barat seperti Nigeria, Kamerun, dan Mali, serta di Asia, seperti India, Sri Lanka, Bangladesh, Iran, Thailand, Malaysia dan Indonesia. Hingga awal abad ke-20, batik yang dihasilkan merupakan batik tulis. Batik cap baru dikenal setelah Perang Dunia I berakhir atau sekitar tahun 1920. </h3>
                    <h3 class="section-subheading text-muted">Kesenian batik adalah kesenian gambar di atas kain untuk pakaian yang menjadi salah satu kebudayaan keluarga kerajaan di Indonesia zaman dahulu. Awalnya kegiatan membatik hanya terbatas dalam keraton saja dan batik dihasilkan untuk pakaian raja dan keluarga pemerintah dan para pembesar. Oleh karena banyak dari pembesar tinggal di luar keraton, maka kesenian batik ini dibawa oleh mereka keluar dari keraton dan dihasilkan pula di tempatnya masing-masing. Lama kelamaan kesenian batik ini ditiru oleh rakyat jelata dan selanjutnya meluas sehingga menjadi pekerjaan kaum wanita rumah tangga untuk mengisi waktu luang mereka. Bahan-bahan pewarna yang dipakai ketika membatik terdiri dari tumbuh-tumbuhan asli Indonesia yang dibuat sendiri antara lain dari: pohon mengkudu, soga, nila. Bahan sodanya dibuat dari soda abu, sedangkan garamnya dibuat dari tanah lumpur.</h3>
                    <h3 class="section-subheading text-muted">Batik adalah kain Indonesia bergambar yang pembuatannya secara khusus dengan menuliskan atau menerakan malam pada kain itu, kemudian pengolahannya diproses dengan cara tertentu yang memiliki kekhasan. Sebagai keseluruhan teknik, teknologi, serta pengembangan motif dan budaya yang terkait, oleh UNESCO telah ditetapkan sebagai Warisan Kemanusiaan untuk Budaya Lisan dan Nonbendawi (Masterpieces of the Oral and Intangible Heritage of Humanity) sejak 2 Oktober 2009..Teknik seni kain yang mirip batik sebenarnya ditemukan dalam beberapa budaya seperti di Nigeria, Tiongkok, India, Malaysia, Sri Lanka dan daerah lain di Indonesia. Namun, batik pesisir Indonesia dari pulau Jawa memiliki sejarah akulturasi yang panjang, dengan corak beragam yang dipengaruhi oleh berbagai budaya, serta paling berkembang dalam hal pola, teknik, dan kualitas pengerjaan dibandingkan batik dari daerah lain.</h3>
                    <h3 class="section-subheading text-muted">Secara etimologi, istilah "batik" berasal dari bahasa Jawa: ꦲꦩ꧀ꦧꦛꦶꦏ꧀, translit. ambathik yang dihasilkan dari lakuran kata ꦲꦩ꧀ꦧ (amba) yang berarti "lebar" atau "luas" (merujuk kepada kain), dan ꦤꦶꦛꦶꦏ꧀ (nithik) yang berarti "membuat titik" dan kemudian berkembang menjadi istilah bahasa Jawa: ꦧꦛꦶꦏ꧀, translit. bathik, yang berarti menghubungkan titik-titik menjadi gambar tertentu pada kain yang luas atau lebar. Kata dalam bahasa Jawa: ꦧꦛꦶꦏꦤ꧀, translit. bathikan juga dapat bermakna sebagai "menggambar" atau "menulis". Istilah bathik kemudian diserap kedalam bahasa Indonesia menjadi "batik" dengan menggantikan bunyi huruf "-th" sebagai "-t" dikarenakan orang non-Jawa tidak bisa melafalkannya dengan mudah. Dalam Kamus Besar Bahasa Indonesia, "batik" didefinisikan sebagai kain bergambar yang pembuatannya secara khusus dengan menuliskan atau menerakan lilin (atau dalam bahasa Jawa: ꦩꦭꦩ꧀, translit. malam) pada kain itu, yang kemudian pengolahannya melalui proses tertentu. Jadi, dapat disimpulkan bahwa "batik" dapat merujuk kepada sebuah proses maupun hasil jadi (bersifat bendawi) dari proses tersebut.</h3>
                    <h3 class="section-subheading text-muted">Dalam literatur Eropa, teknik batik ini pertama kali diceritakan dalam buku History of Java (London, 1817) tulisan Sir Thomas Stamford Raffles. Ia pernah menjadi Gubernur Inggris di Jawa semasa Napoleon menduduki Belanda. Pada 1873 seorang saudagar Belanda Van Rijekevorsel memberikan selembar batik yang diperolehnya saat berkunjung ke Indonesia ke Museum Etnik di Rotterdam dan pada awal abad ke-19 itulah batik mulai mencapai masa keemasannya. Sewaktu dipamerkan di Exposition Universelle di Paris pada tahun 1900, batik Indonesia memukau publik dan seniman. Semenjak industrialisasi dan globalisasi, yang memperkenalkan teknik otomatisasi, batik jenis baru muncul, dikenal sebagai batik cap dan batik cetak, sementara batik tradisional yang diproduksi dengan teknik tulisan tangan menggunakan canting dan malam disebut batik tulis. Pada saat yang sama imigran dari Indonesia ke Wilayah Persekutuan Malaysia juga membawa Batik bersama mereka.</h3>
